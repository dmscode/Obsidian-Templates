const addNum = (arr, numName)=>{
  if(!dict[numName]) return
  dict[numName].forEach((line, i)=>{
    arr[i] = arr[i] ? arr[i]+line : line
  })
}
function main (number) {
  const strArr = []
  addNum(strArr, 'start')
  number.split('').forEach(n=>{
    addNum(strArr, n)
  })
  addNum(strArr, 'end')
  return strArr.join('\n')
}
